package com.igor;

public class User {
	
	String name;
	String email;
	String area;
	
	public String getName() {  
		return name;  
	}
	
	public void setName(String name) {  
		this.name = name;
	}
	
	public String getEmail() {  
		return email;
	}
	
	public void setEmail(String email) {  
		this.email = email;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}
	
}